package com.rns.healthplease.web.bo.domain;

public class HPWeek {
	
	private HPDay monday;
	private HPDay tuesday;
	private HPDay wednesday;
	private HPDay thursday;
	private HPDay friday;
	
	public HPDay getMonday() {
		return monday;
	}
	public void setMonday(HPDay monday) {
		this.monday = monday;
	}
	public HPDay getTuesday() {
		return tuesday;
	}
	public void setTuesday(HPDay tuesday) {
		this.tuesday = tuesday;
	}
	public HPDay getWednesday() {
		return wednesday;
	}
	public void setWednesday(HPDay wednesday) {
		this.wednesday = wednesday;
	}
	public HPDay getThursday() {
		return thursday;
	}
	public void setThursday(HPDay thursday) {
		this.thursday = thursday;
	}
	public HPDay getFriday() {
		return friday;
	}
	public void setFriday(HPDay friday) {
		this.friday = friday;
	}
	public HPDay getSaturday() {
		return saturday;
	}
	public void setSaturday(HPDay saturday) {
		this.saturday = saturday;
	}
	public HPDay getSunday() {
		return sunday;
	}
	public void setSunday(HPDay sunday) {
		this.sunday = sunday;
	}
	private HPDay saturday;
	private HPDay sunday;
	
	
	

}
